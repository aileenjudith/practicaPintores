<img style="float:right;" src="\images\rosseau.jpg" hspace="5" vspace="5" width=250px/>Henri Julien Félix Rousseau, también llamado «el aduanero» fue uno de esos artistas con muy poca o nula formación académica, pero gran frescura, que es exactamente lo que necesitaba el arte moderno definales del siglo XIX. Un arte, el naíf (ingenuo), que combina la ingenuidad formal con una cierta sofisticación temática y compositiva, y que acabaría influyendo notablemente en vanguardias como el fauvismo o el surrealismo, con el mismísimo Picasso a la cabeza. Rousseau no se dedicó a la pintura hasta 1893 (49 años). Antes fue empleado de las oficinas fiscales municipales de París (de ahí su sobrenombre). Una vez abandonado su puesto y metido en el mundo de la pintura fue alabado por sus colegas, pero ignorado o directamente ridiculizado por los críticos. Vivía de una pequeña pensión y de los retratos que pintaba a sus vecinos. Picasso, celebró en 1908 en su taller del Bateau-Lavoir una velada en su honor como una mezcla de burla y homenaje (una soirée como en la película «La cena de los idiotas»). El aduanero, varias copas después dijo al español: «Somos los dos pintores más grandes de nuestra época, tú en el estilo egipcio y yo en el moderno…». Su pintura destaca por un enorme detallismo (después de todo, Rousseau admiraba el arte académico), el uso de colores intensos y modulados sutilmente, la perspectiva acientífica y la temática exótica o directamente onírica. Esta forma de representar los sueños fue muy alabada por los surrealistas años después. Debido a esta falta de academicismo, esa forma de pintar nacida en realidad de su ignorancia, fue precisamente su sello característico y lo que le valió la inmortalidad. Pintar sin saber pintar (quizás si un poquito)… Y a la mierda 5 siglos de tradición pictórica.
