<img style="float:right;" src="\images\maxi.jpg" hspace="5" vspace="5" width=250px/>Max Ernst fue un artista alemán nacionalizado francés considerado figura fundamental tanto en el movimiento dadá como en el surrealismo. A lo largo de su variada carrera artística, Ernst se caracterizó por ser un experimentador infatigable, utilizando una extraordinaria diversidad de técnicas, estilos y materiales. En todas sus obras buscaba los medios ideales para expresar, en dos o tres dimensiones, el mundo extradimensional de los sueños y la imaginación. (Brühl, Alemania, 1891 - París, 1976) Pintor y escultor alemán. Formó parte del movimiento dadaísta de Colonia, aunque desde 1919 ya realizaba asociaciones en sus obras que pueden considerarse surrealistas. Ernst practicó diversas y originales técnicas pictóricas, siempre dentro de la estética de lo absurdo y dictadas por el automatismo característico del surrealismo. Empleó durante toda su vida el procedimiento del collage, como en La mujer de cien cabezas (1929). Mediante el balanceo de una lata de pintura agujereada experimentó la técnica del goteo, claro antecedente del futuro dripping de Jackson Pollock. También usó el recurso de la decalcomanía, con el que duplicaba manchas de forma simétrica, y el método del frottage, que consistía en la reproducción de texturas, al pintar sobre un papel superpuesto a una superficie con relieves. Con este procedimiento trabajó sus colecciones de Bosques y de la Historia Natural entre 1926 y 1928. Gran parte de su producción se caracteriza por una temática inquietante y por la aparición de formas orgánicas y minerales que crean una atmósfera de pesadilla.
